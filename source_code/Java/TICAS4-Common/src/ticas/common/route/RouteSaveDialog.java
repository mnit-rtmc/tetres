    private void saveRoute() {
        String name = this.tbxRouteName.getText();
        String desc = this.tbxRouteDesc.getText();
        if (!checkName(name)) {
            return;
        }
        Route r = new Route(name, desc);
        for (RNode rn : this.rnodes) {
            r.addRNode(rn);
        }
        isSaved = LocalRouteClient.getInstance().saveRoute(r);
        if (isSaved) {
            routeApi.loadList();
            dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, String.format("Fail to save route"));
        }
    }

    private boolean checkName(String name) {
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Fail : Route name is required");
            return false;
        }
        if (name.length() > 128) {
            JOptionPane.showMessageDialog(rootPane, "Fail : Route name is long. (128 characters limit)");
            return false;
        }
        String eName = Route.getFileName(name);
        if (eName == null) {
            JOptionPane.showMessageDialog(rootPane, "Fail : MD5 algorithm does not exist!!");
            return false;
        }
        Route exRoute = routeApi.getRoute(name);
        if (exRoute != null) {
            int res = JOptionPane.showConfirmDialog(rootPane, "Route name already exists. Overwrite the route?");
            if (res > 0) {
                return false;
            }
        }
        return true;
    }

