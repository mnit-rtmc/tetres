    public void init() {
        Infra infra = Infra.getInstance();
        this.corridors = infra.getCorridors();
        
        this.jxMap.getMiniMap().setVisible(false);
        this.mapHelper = new MapHelper(jxMap);
        
        // add click-event handler for a route list
        this.tbRouteList.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int row = tbRouteList.rowAtPoint(e.getPoint());
                DefaultTableModel tm = (DefaultTableModel) tbRouteList.getModel();
                Route s = (Route) tm.getValueAt(row, 0);
                setProperties(s);
            }
        });
        
        LocalRouteClient.getInstance().addChangeListener(this);
        LocalRouteClient.getInstance().loadList();               
    }

    protected void setProperties(Route s) {
        this.mapHelper.showRoute(s);
        this.mapHelper.setCenter(s.getRNodes().get(0));
        this.selectedRoute = s;
    }

    @Override
    public void routeChanged(List<Route> routeList) {
        if (this.tbRouteList == null) {
            return;
        }
        final DefaultTableModel tm = (DefaultTableModel) this.tbRouteList.getModel();
        tm.getDataVector().removeAllElements();
        tm.fireTableDataChanged();
        for (Route s : routeList) {
            tm.addRow(new Object[]{s, s.desc, "Delete"});
        }
        if (routeList.size() > 0) {
            Route s = (Route) tm.getValueAt(0, 0);
            if (s != null) {
                setProperties(s);
            }
        }
    }

    protected void deleteRoute() {
        if (this.selectedRoute == null) {
            return;
        }
        int res = JOptionPane.showConfirmDialog(this, "Delete this route?", "Confirm", JOptionPane.YES_NO_OPTION);

        if (res == JOptionPane.YES_OPTION) {
            LocalRouteClient.getInstance().deleteRoute(this.selectedRoute);
            this.selectedRoute = null;
            LocalRouteClient.getInstance().loadList();
        }
    }

    protected void saveRouteConfig(Route r) {
        RouteLaneConfigDialog rlcd = new RouteLaneConfigDialog(r, null, true);
        rlcd.setVisible(true);
    }

